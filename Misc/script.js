
$("#leatherName").hover(function () {
    $("#leatherName").css("color", "#133d8a");
    $("#leatherName").css("background-color", "#ffc400");
    $("#leatherName").css("transform", "scale(1.1)");
    $("#leatherName").css("transform", "rotate(5deg)");
    $("#leatherName").css("cursor", "pointer");
},
    function () {
        $("#leatherName").css("color", "##faf7f7");
        $("#leatherName").css("background-color", "#133d8a");
        $("#leatherName").css("transform", "scale(1)");
        $("#leatherName").css("transform", "rotate(0deg)");
        $("#leatherName").css("cursor", "default");
    })


