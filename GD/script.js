
$("#posterName").hover(function () {
    $("#posterName").css("color", "#133d8a");
    $("#posterName").css("background-color", "#ffc400");
    $("#posterName").css("transform", "scale(1.1)");
    $("#posterName").css("transform", "rotate(5deg)");
    $("#posterName").css("cursor", "pointer");
},
    function () {
        $("#posterName").css("color", "##faf7f7");
        $("#posterName").css("background-color", "#133d8a");
        $("#posterName").css("transform", "scale(1)");
        $("#posterName").css("transform", "rotate(0deg)");
        $("#posterName").css("cursor", "default");
    })


